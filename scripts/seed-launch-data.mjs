import { getRuntime } from '../server/runtime.js';

const args = new Set(process.argv.slice(2));
const runtime = getRuntime();
const { serverEnv, services } = runtime;

if (serverEnv.nodeEnv === 'production' && !args.has('--allow-production')) {
  console.error('Refusing to seed production without --allow-production.');
  process.exit(1);
}

const installationId = 'seed_installation_demo';
const userId = 'seed_google_demo_user';
const identity = { installationId, userId };
const now = new Date().toISOString();

const seededProfile = {
  id: 'me',
  name: 'Seed Demo',
  gender: 'female',
  knowledgeLevel: 'intermediate',
  birthDate: { year: 1994, month: 4, day: 5, hour: 11, minute: 30, ampm: 'AM' },
  calendarType: 'solar',
  isTimeUnknown: false,
  relation: 'me',
  memo: 'Seeded for local launch QA.',
  avatarId: 0,
};

const shareMetadata = {
  inviteId: 'invite_seed_demo',
  source: 'daily_fortune',
  targetTab: 'home',
  inviterName: seededProfile.name,
  previewTitle: 'Shared comparison',
  previewSummary: 'A seeded share card is ready for QA.',
  comparisonSummary: 'Your timing complements each other.',
  shareUrl: 'https://sazoo.vercel.app/?invite=seed-demo',
  language: 'ko',
  createdAt: now,
};

const specialReport = {
  id: 'invite_comparison_seed_demo',
  type: 'invite_comparison',
  title: 'Seeded comparison report',
  summary: 'This report was generated by the local seed script.',
  sourceInviteId: shareMetadata.inviteId,
  unlockedAt: now,
};

await services.userStateStore.saveState(identity, {
  profiles: [seededProfile],
  activeProfileId: 'me',
  userTier: 'FREE',
  onboardingComplete: true,
});

await services.walletStore.credit(identity, 2, 'manual_adjustment');
await services.unlockStore.upsertSpecialReport(identity, specialReport);
await services.profileMemoryStore.upsertMemory(identity, 'me', {
  version: 'phase4.v2',
  knowledgeLevel: 'intermediate',
  preferredTone: 'mysterious_intimate',
  primaryConcerns: ['career', 'relationship'],
  recurringTopics: ['career'],
  relationshipContext: { relation: 'self', focus: 'balance' },
  recentSummary: 'User asked for a grounded reading with practical advice.',
  conversationDigest: 'Career pacing and relationship balance were recurring themes.',
  openLoops: ['Decide whether to change jobs this spring'],
  lastAssistantGuidance: 'Move slowly and verify the next opportunity before jumping.',
  lastUserQuestions: ['Should I change jobs this spring?'],
});
await services.chatSummaryStore.upsertSummary(identity, 'me', {
  recentSummary: 'Career pacing and timing are the latest focus.',
  conversationDigest: 'The user wants a calm but actionable reading.',
  openLoops: ['Job move timing'],
  lastAssistantGuidance: 'Keep a measured pace and review the next offer carefully.',
  updatedAt: now,
});
await services.authIdentityStore.upsertIdentity(identity, {
  provider: 'google',
  providerAccountId: userId,
  displayName: seededProfile.name,
  email: 'seed-demo@example.com',
  photoURL: null,
  lastLoginAt: now,
});
await services.shareMetadataStore.upsertMetadata(identity, shareMetadata);

console.log(JSON.stringify({
  seeded: true,
  env: serverEnv.nodeEnv,
  durableDatabase: Boolean(serverEnv.databaseUrl),
  userId,
  installationId,
  inviteId: shareMetadata.inviteId,
}, null, 2));
